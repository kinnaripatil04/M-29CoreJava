//Program to demonstrate on various data types
package org.tnsif.introduction;

public class DataTypesDemo {

	public static void main(String[] args) {
		//integer will give value in whole number
		//float and double will give values in decimal numbers
		//if we use in programming division '/' it prints quotient value and modulo will be done by %
		int num1 = 5/2;
		float num2 = 103/45;
		double num3 = 10d/3d;
		//if we use f it will give precision upto 7 decimals
		//if we use d it will give precision upto 16 decimals
		float num4 = 14f/3f;
		System.out.println("The value1 is: "+num1);
		System.out.println("The value2 is: "+num2);
		System.out.println("The value3 is: "+num3);
		System.out.println("The value4 is: "+num4);
	}

}
